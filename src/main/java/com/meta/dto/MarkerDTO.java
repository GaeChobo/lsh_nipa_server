	public void setMarker_detail(String marker_detail) {
		this.marker_detail = marker_detail;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
